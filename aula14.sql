/*
	Modelo Relacional 
    
    na decade de 60 foi feito o codazil e onde surgiu cobol e os modelos hierarquicos 
    
    na decada de 70 foi criado um modelo relacional 
    
    toda entidade possui uma coleção de atributos 
    
    dados são representados em forma de atributos
    conjuntos de atributos vão identifcar conjunto de registros 
    que vão identificar entidades
    
    
    o modelo relacional serve para fazer ligações entre entidades 
    
    um conjunto de atributos serve para identificar cada uma das tuplas(registros)
    
    atributos chave primárias serve para identificar tuplas e relacionar entidades
    
    ER Diagrama entidade relacionamento 
    DER 
    Relacionamentos
    1 para 1 
    1 para * n (muitos)
    * para * (muitos para muitos)
    
    Chave primaria: é um atributo especifico que identifica as tuplas(registros)
    chave estrangeira:é a chave primaria que vem de alguma outra entidade(tabela)  
*/